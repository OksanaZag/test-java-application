public class MathFunction {

    public MathFunction(){
    
    }   
    
    public long sum(int operand1, int operand2){
        return operand1+operand2;
    }
    
    public long division(int operand1, int operand2){
        return operand1/operand2;
     }
    
    public long multiplication(int operand1, int operand2){
        return operand1*operand2;
    }
    
    public long subtraction(int operand1, int operand2){
        return operand1-operand2;
    }
}
